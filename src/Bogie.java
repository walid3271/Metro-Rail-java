    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!((bogie.getText()).equals("") || (seats.getText()).equals(""))) {
            String sql = "INSERT INTO inside_train(Bogie, Seats, T_ID, Status) VALUES (?,?,?,?)";

            try {
                pst = con.prepareStatement(sql);
                pst.setString(1, bogie.getText());
                pst.setString(2, seats.getText());
                pst.setString(3, tId);
                pst.setString(4, "Available");
                pst.execute();
                JOptionPane.showMessageDialog(null, "Insert Successfully.");
                
                bogietableUpdate();

                bogie.setText("");
                seats.setText("");
                bogie.requestFocus();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Internal problem."+ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Fill all the boxes.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

