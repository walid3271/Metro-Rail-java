    private void bogietableUpdate() {
        int CC;
        try {
            pst = con.prepareStatement("Select * From inside_train Where T_ID=?");
            //String sql ="Select * From inside_train Where T_ID=?";
            //try {
                //pst = con.prepareStatement(sql);
                pst.setString(1, tId);
                //pst.execute();
            //}
            
           // catch (Exception ex) {
                //JOptionPane.showMessageDialog(null, "Problem.");
           // }
            
            ResultSet Rs = pst.executeQuery();

            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();

            DefaultTableModel DFT = (DefaultTableModel) bogieTable.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= CC; i++) {
                    v2.add(Rs.getString("T_ID"));
                    v2.add(Rs.getString("Bogie"));
                    v2.add(Rs.getString("Seats"));
                    v2.add(Rs.getString("Status"));
                }
                DFT.addRow(v2);

            }
        } catch (Exception ex) {

        }
    }
